#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t i;

	if (s != NULL && f != NULL)
	{
		i = 0;
		if (s[i] != 0)
		{
			while (s[i])
			{
				(*f)(i, &s[i]);
				i++;
			}
		}
	}
}
