#include "libft.h"

int		ft_atoi(const char *a)
{
	int i;
	int atoi;
	int n;

	i = 0;
	atoi = 0;
	n = 1;
	while (a[i] == ' ' || a[i] == '\n' || a[i] == '\t' || a[i] == '\v' || \
	a[i] == '\r' || a[i] == '\f')
		i++;
	if (a[i] == '-' && a[i + 1] >= 48 && a[i + 1] <= 57)
	{
		i++;
		n = 0;
	}
	if (a[i] == '+' && a[i + 1] >= 48 && a[i + 1] <= 57)
		i++;
	while (a[i] >= 48 && a[i] <= 57)
	{
		atoi = (atoi * 10) + (a[i] - 48);
		i++;
	}
	return (n ? atoi : -atoi);
}
