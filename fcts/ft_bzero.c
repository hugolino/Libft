#include "libft.h"

void		ft_bzero(void *s, size_t n)
{
	size_t	a;
	int		b;
	char	*tmp;

	a = 0;
	b = 0;
	tmp = s;
	while (a < n)
	{
		tmp[a] = b;
		a++;
	}
}
